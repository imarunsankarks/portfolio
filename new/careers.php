    <script>
    function selectAll(arg) {
        const allJobs = document.querySelectorAll('.job-card');
        const noItem = document.querySelector('.no-items');
        if (noItem) {
            noItem.remove();
        }

        if (arg === 'all') {
            allJobs.forEach((item) => {
                item.classList.remove('d-none');
            });
            location.reload()
        } else {
            allJobs.forEach((item) => {
                item.classList.add('d-none');
            });
            const jobs = document.querySelectorAll('.' + arg);
            if (jobs.length > 0) {
                jobs.forEach((item) => {
                    item.classList.remove('d-none');
                })
            } else {
                const tabContentDiv = document.querySelector('.tab-content');

                if (tabContentDiv) {
                    const tabContentP = document.querySelector('.no-items');
                    if (tabContentP == null) {
                        const newParagraph = document.createElement('p');
                        newParagraph.classList.add('no-items');
                        newParagraph.textContent = 'Currently no vacancies';
                        tabContentDiv.appendChild(newParagraph);
                    }
                } else {
                    console.error('Could not find element with class name "tab-content".');
                }

            }
            smoothScroll("main");
        }
    }
    </script>




