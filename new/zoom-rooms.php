        let bannerSection = document.querySelector('.brand-card');

        gsap.to('.brand-card', {
            x: () => bannerSection.scrollWidth * -1,
            xPercent: 100,
            scrollTrigger: {
                trigger: '.brand-card',
                start: 'center-=30px center',
                end: '+=1000px',
                pin: '#brand-banner',
                scrub: true,
                // markers:true,
                invalidateOnRefresh: true,
            }
        });

